package model;

public class Giuong {
	private int id;
	private int tanggiuong;
	private int gia;
	private Phong phong;
	public Giuong() {
		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getTanggiuong() {
		return tanggiuong;
	}
	public void setTanggiuong(int tanggiuong) {
		this.tanggiuong = tanggiuong;
	}
	public int getGia() {
		return gia;
	}
	public void setGia(int gia) {
		this.gia = gia;
	}
	public Phong getPhong() {
		return phong;
	}
	public void setPhong(Phong phong) {
		this.phong = phong;
	}
	
	
	
	
}
